# ********************< 浮点数运算 >********************
# 第一种方法(bc)
$(echo "scale=2; $a / $b" | bc)
# 第二种方法(awk)
$(awk 'BEGIN {print '$a' / '$b'}')
$(awk 'BEGIN {printf "%.2f\n", '$a' / '$b'}')

# ********************< 百分数(50.00%) >********************
$(awk 'BEGIN {printf "%.2f%\n", '$a' / '$b' *100}')