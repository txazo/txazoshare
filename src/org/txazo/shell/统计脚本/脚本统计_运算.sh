# ********************< 四则运算(加 减 乘 除 模) >********************
a=1
b=2
# 第一种方法(运算符前后无空格)
let c=a+b | let c=$a+$b
# 第二种方法(运算符前后可空格)
c=$[a + b] | c=$[$a + $b]
# 第三种方法(运算符前后可空格)
c=$((a + b)) | c=$(($a + $b))
# 第四种方法(运算符前后可空格)
c=`expr $a + $b`
# 第五种方法(运算符前后可空格)
c=$(expr $a + $b)

# ``和$()等效
